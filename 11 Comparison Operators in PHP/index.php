<?php
    $a = 10;
    $b = 20;

    echo $a == $b; // Prints "" for false

    echo $a !== $b; // Prints 1 for true
    
    echo $a <> $b; // Prints 1 for true
    
    echo $a > $b; // Prints "" for false
    
    echo $a < $b; // Prints 1 for true
    
    echo $a >= $b; // Prints "" for false
    
    echo $a <= $b; // Prints 1 for true
    
    echo $a <=> $b; // Prints -1 for less left, 1 for greater right and 0 for both equal


?>